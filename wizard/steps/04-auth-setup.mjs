// wizard/steps/04-auth-setup.mjs — Credential storage, PAC profiles, connection verification
import { select, input, password, confirm } from '@inquirer/prompts';
import { writeFileSync, readFileSync, existsSync, appendFileSync, mkdirSync, copyFileSync, chmodSync } from 'node:fs';
import { join } from 'node:path';
import { platform } from 'node:os';
import * as ui from '../lib/ui.mjs';
import { stateGet, stateSet, setCompletedStep, TOTAL_STEPS, getRootDir } from '../lib/state.mjs';
import { pacPath, hasCommand, runSafeCapture, runSafeLive } from '../lib/shell.mjs';
import { getSecret, recoverSecret, setSecret } from '../lib/secrets.mjs';
import { encrypt } from '../lib/crypto.mjs';
import {
  buildPacProfileName,
  getWizardStateSnapshot,
  resolveCredentialValues,
  selectAndVerifyPacProfile,
} from '../lib/pac-target.mjs';

export default async function stepAuthSetup() {
  ui.stepHeader(4, TOTAL_STEPS, 'Setting Up Authentication');

  const ROOT = getRootDir();
  const hasOp = hasCommand('op');
  const tenantId = stateGet('PP_TENANT_ID');
  const clientId = stateGet('PP_APP_ID');
  const devUrl = stateGet('PP_ENV_DEV');
  const testUrl = stateGet('PP_ENV_TEST', '');
  const prodUrl = stateGet('PP_ENV_PROD', '');

  // Retrieve or recover the client secret
  let secret = getSecret();
  if (!secret) secret = recoverSecret();
  if (!secret) {
    ui.line('We need your Client Secret again (it\'s not stored in memory across sessions).');
    const entered = await password({ message: 'Client Secret (hidden input)', validate: (v) => v ? true : 'Required' });
    setSecret(entered);
    secret = entered;
    ui.line('');
  }

  // ── Auth mode — may already be set from Step 03 (1Password) ──
  let authMode = stateGet('AUTH_MODE', '');

  if (authMode === '1password' && hasOp) {
    // Already chose 1Password in Step 03 — skip the question
    ui.line('Using 1Password for credential storage (selected in previous step).');
    ui.line('');
  } else {
    // Ask for storage mode
    const choices = [];
    if (hasOp) {
      choices.push({ name: '1Password (secrets never touch disk) — RECOMMENDED', value: '1password' });
    } else {
      choices.push({ name: '1Password — UNAVAILABLE (op CLI not found)', value: '1password', disabled: true });
    }
    choices.push({ name: '.env.local file (secrets on disk, gitignored)', value: 'envlocal' });

    authMode = await select({ message: 'How do you want to store credentials?', choices });
    stateSet('AUTH_MODE', authMode);
    stateSet('HAS_OP', hasOp);
  }

  ui.line('');
  const pac = pacPath();
  const dateStr = new Date().toISOString().slice(0, 10);
  const opBin = hasOp ? 'op' : null;
  let persistedCredentialValues = null;

  if (authMode === '1password') {
    // ── 1Password setup ──
    const appName = stateGet('APP_NAME');
    // Use vault/item from Step 03 if already set, otherwise prompt
    let vault = stateGet('OP_VAULT', '');
    let itemName = stateGet('OP_ITEM', '');
    if (!vault) vault = await input({ message: '1Password vault name', default: 'Engineering' });
    if (!itemName) itemName = await input({ message: '1Password item name', default: `PowerApps CodeApps - ${appName}` });
    stateSet('OP_VAULT', vault);
    stateSet('OP_ITEM', itemName);

    ui.line('');
    ui.line(`Make sure the 1Password item "${itemName}" exists in vault "${vault}"`);
    ui.line('with these EXACT field labels:');
    ui.line('  • tenant-id      (Text)');
    ui.line('  • app-id         (Text)');
    ui.line('  • client-secret  (Password)');
    ui.line('  • env-dev        (Text)');
    if (testUrl) ui.line('  • env-test       (Text)');
    if (prodUrl) ui.line('  • env-prod       (Text)');
    ui.line('');

    // Write .env with op:// references (safe to commit)
    let envContent = `# .env — Safe to commit. Contains 1Password references, not secrets.\n`;
    envContent += `# Generated by setup wizard on ${dateStr}\n\n`;
    envContent += `PP_TENANT_ID=op://${vault}/${itemName}/tenant-id\n`;
    envContent += `PP_APP_ID=op://${vault}/${itemName}/app-id\n`;
    envContent += `PP_CLIENT_SECRET=op://${vault}/${itemName}/client-secret\n`;
    envContent += `PP_ENV_DEV=op://${vault}/${itemName}/env-dev\n`;
    if (testUrl) envContent += `PP_ENV_TEST=op://${vault}/${itemName}/env-test\n`;
    if (prodUrl) envContent += `PP_ENV_PROD=op://${vault}/${itemName}/env-prod\n`;
    writeFileSync(join(ROOT, '.env'), envContent, 'utf-8');
    ui.ok('Wrote .env with 1Password references');

  } else {
    // ── .env.local setup ──
    // Ensure .gitignore has .env.local BEFORE writing the file (prevents race with git add)
    const gitignorePath = join(ROOT, '.gitignore');
    if (existsSync(gitignorePath)) {
      const gi = readFileSync(gitignorePath, 'utf-8');
      if (!gi.includes('.env.local')) {
        appendFileSync(gitignorePath, '\n.env.local\n');
        ui.ok('Added .env.local to .gitignore');
      } else {
        ui.ok('.env.local already in .gitignore');
      }
    } else {
      writeFileSync(gitignorePath, '.env.local\n', 'utf-8');
      ui.ok('Created .gitignore with .env.local');
    }

    let envContent = `# .env.local — DO NOT commit to Git.\n`;
    envContent += `# Generated by setup wizard on ${dateStr}\n\n`;
    envContent += `PP_TENANT_ID=${tenantId}\n`;
    envContent += `PP_APP_ID=${clientId}\n`;
    envContent += `PP_CLIENT_SECRET=${encrypt(secret)}\n`;
    envContent += `PP_ENV_DEV=${devUrl}\n`;
    if (testUrl) envContent += `PP_ENV_TEST=${testUrl}\n`;
    if (prodUrl) envContent += `PP_ENV_PROD=${prodUrl}\n`;
    writeFileSync(join(ROOT, '.env.local'), envContent, 'utf-8');
    // Restrict to owner-only on Unix (contains encrypted secrets)
    if (platform() !== 'win32') {
      try { chmodSync(join(ROOT, '.env.local'), 0o600); } catch { /* best-effort */ }
    }
    ui.ok('Wrote .env.local (owner-only permissions)');
  }

  try {
    persistedCredentialValues = resolveCredentialValues({ rootDir: ROOT, opBin });
  } catch (error) {
    ui.fail(`Could not resolve persisted credentials: ${error.message}`);
    process.exit(1);
  }

  if (pac) {
    try {
      createProfiles(pac, persistedCredentialValues, { devUrl, testUrl, prodUrl });
    } catch (error) {
      ui.fail(error.message);
      process.exit(1);
    }
  }

  // ── Install pre-commit hook ──
  installPreCommitHook(ROOT);

  // ── .env.template ──
  ui.line('');
  let tpl = `# .env.template — Copy to .env.local and fill in values\n`;
  tpl += `# DO NOT commit .env.local to Git\n\n`;
  tpl += `PP_TENANT_ID=\nPP_APP_ID=\nPP_CLIENT_SECRET=\n`;
  tpl += `PP_ENV_DEV=${devUrl}\n`;
  if (testUrl) tpl += `PP_ENV_TEST=${testUrl}\n`;
  if (prodUrl) tpl += `PP_ENV_PROD=${prodUrl}\n`;
  writeFileSync(join(ROOT, '.env.template'), tpl, 'utf-8');
  ui.ok('Wrote .env.template');

  // ── Verify connection ──
  if (pac) {
    ui.line('');
    ui.line('Verifying connection to the wizard target environment...');
    try {
      const verification = selectAndVerifyPacProfile({
        pac,
        rootDir: ROOT,
        wizardState: getWizardStateSnapshot(stateGet),
        targetKey: stateGet('WIZARD_TARGET_ENV', 'dev'),
        profileType: 'spn',
        credentialValues: persistedCredentialValues,
        powerConfigPath: join(ROOT, 'power.config.json'),
        requireCredentialMatch: true,
        requirePowerConfig: false,
        requirePowerConfigTarget: false,
      });
      ui.ok('Connected successfully!');
      console.log(verification.whoInfo.raw.split('\n').map((l) => `  ${l}`).join('\n'));
    } catch (error) {
      ui.fail(error.message);
      process.exit(1);
    }

    // ── Offer to create user (interactive) auth profile ──
    // pac code commands (push, add-data-source, run) reject SPN tokens.
    // Creating the user profile now prevents a confusing auth failure in steps 7-9.
    ui.line('');
    ui.warn('All pac code commands (push, add-data-source, run) require user auth — SPN is rejected.');
    ui.line('  Creating a user auth profile now avoids a confusing failure later.');
    ui.line('');
    const targetKey = stateGet('WIZARD_TARGET_ENV', 'dev');
    const userProfileName = buildPacProfileName({ rootDir: ROOT, targetKey, profileType: 'user', url: devUrl });
    const createUser = await confirm({ message: 'Create a user (interactive) auth profile now?', default: true });
    if (createUser) {
      ui.line('');
      ui.line('Sign-in method:');
      ui.line('  1. Browser (opens automatically — fastest when it works)');
      ui.line('  2. Device code (paste a code into any browser — most reliable)');
      const signInMethod = await select({
        message: 'How would you like to sign in?',
        choices: [
          { name: 'Device code — most reliable', value: 'deviceCode' },
          { name: 'Browser — may hang on some macOS setups', value: 'browser' },
        ],
        default: 'deviceCode',
      });

      let userOk = false;
      if (signInMethod === 'browser') {
        ui.line('');
        ui.line('Opening browser for sign-in...');
        ui.line('If this terminal appears frozen for more than ~2 minutes after you finish signing in,');
        ui.line('press Enter here or Ctrl+C and pick device code instead.');
        ui.line('');
        // 3-minute timeout so a stuck browser callback cannot hang the wizard forever.
        userOk = runSafeLive(pac, [
          'auth', 'create', '--name', userProfileName,
          '--environment', devUrl,
        ], { timeout: 180000 });
        if (!userOk) {
          ui.warn('Browser sign-in did not complete (timed out or failed). Falling back to device code.');
          ui.line('');
        }
      }

      if (!userOk) {
        ui.line('Device code flow: you will see a URL and code below.');
        ui.line('Open the URL in any browser, paste the code, and sign in.');
        ui.line('');
        userOk = runSafeLive(pac, [
          'auth', 'create', '--name', userProfileName,
          '--environment', devUrl, '--deviceCode',
        ]);
      }
      if (userOk) {
        ui.ok(`User profile ${userProfileName} created — pac code commands will work.`);
        // Switch back to SPN profile for steps 5-6 (publisher/solution creation via Dataverse API)
        const spnProfileName = buildPacProfileName({ rootDir: ROOT, targetKey, profileType: 'spn', url: devUrl });
        runSafeCapture(pac, ['auth', 'select', '--name', spnProfileName]);
        ui.ok(`Switched back to SPN profile for next steps.`);
      } else {
        ui.warn(`Could not create user profile. You can create it later before step 7:`);
        ui.line(`  ${pac} auth create --name ${userProfileName} --environment ${devUrl} --deviceCode`);
      }
    } else {
      ui.line('');
      ui.line('You can create it later before running pac code commands:');
      ui.line(`  ${pac} auth create --name ${userProfileName} --environment ${devUrl} --deviceCode`);
    }
  }

  // NOTE: Do NOT clear the secret. Steps 5-6 need it for Dataverse API calls.
  setCompletedStep(4);
}

function createProfiles(pac, credentialValues, stateUrls) {
  const tenantId = credentialValues.PP_TENANT_ID;
  const clientId = credentialValues.PP_APP_ID;
  const secret = credentialValues.PP_CLIENT_SECRET;

  // Environment URLs come from wizard state (the source of truth used by the
  // verification step). Using credentialValues.PP_ENV_* here would cause the
  // profile name hash to diverge from the verify-side hash whenever the
  // credential source (e.g. a pre-existing 1Password item) contains a stale
  // env-dev/env-test/env-prod value from a previous project.
  const devUrl = (stateUrls?.devUrl || '').trim();
  const testUrl = (stateUrls?.testUrl || '').trim();
  const prodUrl = (stateUrls?.prodUrl || '').trim();

  if (!tenantId || !clientId || !secret || !devUrl) {
    throw new Error('Resolved credentials are incomplete. Expected PP_TENANT_ID, PP_APP_ID, PP_CLIENT_SECRET (from the credential source) and PP_ENV_DEV (from wizard state) to be present.');
  }

  // Detect drift between the credential source and wizard state so the user
  // knows to fix the 1Password item (or .env.local) before running anything
  // that calls `resolveCredentialValues` downstream (scripts, step 09, etc.).
  warnOnUrlDrift('PP_ENV_DEV', devUrl, credentialValues.PP_ENV_DEV);
  if (testUrl) warnOnUrlDrift('PP_ENV_TEST', testUrl, credentialValues.PP_ENV_TEST);
  if (prodUrl) warnOnUrlDrift('PP_ENV_PROD', prodUrl, credentialValues.PP_ENV_PROD);

  ui.line('');
  ui.line('Creating PAC auth profiles...');
  createPacProfile(pac, 'dev', devUrl, clientId, secret, tenantId);
  if (testUrl) createPacProfile(pac, 'test', testUrl, clientId, secret, tenantId);
  if (prodUrl) createPacProfile(pac, 'prod', prodUrl, clientId, secret, tenantId);
}

function warnOnUrlDrift(key, stateUrl, credentialUrl) {
  if (!credentialUrl) return;
  const a = String(stateUrl).trim().replace(/\/+$/, '').toLowerCase();
  const b = String(credentialUrl).trim().replace(/\/+$/, '').toLowerCase();
  if (a === b) return;
  ui.warn(`${key} in your credential source (${credentialUrl}) differs from wizard state (${stateUrl}).`);
  ui.line('  PAC profile names are derived from the wizard state URL (source of truth).');
  ui.line('  Update the credential source so downstream scripts stay in sync:');
  ui.line('    • 1Password: edit the item and set the env-* field to match wizard state');
  ui.line('    • .env.local: edit the file and correct the PP_ENV_* line');
}

function createPacProfile(pac, targetKey, url, appId, secret, tenant) {
  const profileName = buildPacProfileName({
    rootDir: getRootDir(),
    targetKey,
    profileType: 'spn',
    url,
  });
  // NOTE: PAC CLI requires --clientSecret as a CLI argument — no env var or stdin mode exists.
  // execFileSync with array args avoids shell history exposure. The brief `ps` visibility
  // during command execution is accepted as a PAC CLI limitation on single-user dev machines.
  const { ok, stderr, stdout } = runSafeCapture(pac, [
    'auth', 'create', '--name', profileName, '--environment', url,
    '--applicationId', appId, '--clientSecret', secret, '--tenant', tenant,
  ]);
  if (ok) {
    ui.ok(`${profileName} profile created`);
  } else {
    throw new Error(formatPacAuthCreateError(profileName, url, stderr || stdout));
  }
}

function formatPacAuthCreateError(profileName, url, output = '') {
  const trimmed = String(output || '').trim();
  const lines = trimmed.split(/\r?\n/).map((line) => line.trim()).filter(Boolean);
  const detail = lines.find((line) => /^Error:/i.test(line)) || lines.at(-1) || 'pac auth create failed without a readable error message.';

  return [
    `${profileName} profile failed to create for ${url}.`,
    `PAC reported: ${detail}`,
    'Check these values before retrying:',
    '1. The app registration is added as an Application User in the target environment.',
    '2. The client secret is current and has not expired or been rotated.',
    '3. The selected credential source points at the intended environment URL for this target.',
  ].join('\n');
}

function installPreCommitHook(root) {
  const hookDir = join(root, '.git', 'hooks');
  const hookPath = join(hookDir, 'pre-commit');
  const hookSource = join(root, 'scripts', 'pre-commit-hook.sh');
  if (!existsSync(join(root, '.git'))) return; // no git repo yet
  if (!existsSync(hookSource)) return;          // hook script not in template
  if (existsSync(hookPath)) {
    // Don't overwrite an existing custom hook
    const existing = readFileSync(hookPath, 'utf-8');
    if (!existing.includes('papps-secret-guard')) return;
  }
  try {
    mkdirSync(hookDir, { recursive: true });
    copyFileSync(hookSource, hookPath);
    chmodSync(hookPath, 0o755);
    ui.ok('Installed pre-commit hook (secret leak protection)');
  } catch {
    ui.warn('Could not install pre-commit hook — install manually:');
    ui.line('  cp scripts/pre-commit-hook.sh .git/hooks/pre-commit && chmod +x .git/hooks/pre-commit');
  }
}
